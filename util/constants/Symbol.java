package util.constants;

/**
 * {@link Symbol}
 *
 * @author <a href="mailto:mercyblitz@gmail.com">Mercy<a/>
 */
public interface Symbol {
    // 符号

    /**
     * Dot : "."
     */
    String DOT = ".";

    /**
     * Class : "class"
     */
    String CLASS = "class";

    /**
     * And : "&"
     */
    String AND = "&";

    /**
     * Equal : "="
     */
    String EQUAL = "=";

    /**
     * BACKSLASH: "\"
     */
    String BACKSLASH = "\\";

    /**
     * FULL_STOP: "。"
     */
    String FULL_STOP = "。";

    /**
     * SEMICOLON: ";"
     */
    String SEMICOLON = ";";
    /**
     * POUND_SIGN: "#"; `#`有很多种读法
     */
    String POUND_SIGN = "#";
}
