package util.constants;

/**
 * Path Constants Definition
 *
 * @author zqw
 */
public interface PathConstants {

    /**
     * Slash : <code>"/"</code>
     */
    String SLASH = "/";
    /**
     * Double Slash : <code>"//"</code>
     */
    String DOUBLE_SLASH = SLASH + SLASH;
    /**
     * Backslash : <code>"\"</code>
     */
    String BACK_SLASH = "\\";

}
